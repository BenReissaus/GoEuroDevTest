import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * Created by Benjamin Reissaus on 15/02/16.
 */
public class City {


    @JsonProperty("_id")
    private String id;
    private String key;
    private String name;
    private String fullName;

    @JsonProperty("iata_airport_code")
    private String iataAirportCode;
    private String type;
    private String country;
    private String locationId;
    private String inEurope;
    private String countryCode;

    @JsonProperty("geo_position")
    private GeoPosition geoPosition;
    private String coreCountry;
    private String distance;

    public City() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFullName() {
        return fullName;
    }

    public void setFullName(String fullName) {
        this.fullName = fullName;
    }

    public String getIataAirportCode() {
        return iataAirportCode;
    }

    public void setIataAirportCode(String iataAirportCode) {
        this.iataAirportCode = iataAirportCode;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getLocationId() {
        return locationId;
    }

    public void setLocationId(String locationId) {
        this.locationId = locationId;
    }

    public String getInEurope() {
        return inEurope;
    }

    public void setInEurope(String inEurope) {
        this.inEurope = inEurope;
    }

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public GeoPosition getGeoPosition() {
        return geoPosition;
    }

    public void setGeoPosition(GeoPosition geoPosition) {
        this.geoPosition = geoPosition;
    }

    public String getCoreCountry() {
        return coreCountry;
    }

    public void setCoreCountry(String coreCountry) {
        this.coreCountry = coreCountry;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }
}
